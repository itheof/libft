#include "libft.h"

char	*ft_strtrim(char const *s)
{
	char	*r;
	size_t	len;
	size_t	i;

	len = 0;
	i = 0;
	while (s[i])
	{
		if (s[i] != ' ' && s[i] != '\n' && s[i] != '\t')
			len++;
		i++;
	}
	
}
